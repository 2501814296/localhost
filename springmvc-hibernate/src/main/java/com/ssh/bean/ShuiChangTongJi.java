package com.ssh.bean;

public class ShuiChangTongJi {
    private int id;
    private int sid;
    private int chanshui;
    private int haodian;
    private int gongshui ;
    private String date;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getSid() {
        return sid;
    }

    public void setSid(int sid) {
        this.sid = sid;
    }

    public int getChanshui() {
        return chanshui;
    }

    public void setChanshui(int chanshui) {
        this.chanshui = chanshui;
    }

    public int getHaodian() {
        return haodian;
    }

    public void setHaodian(int haodian) {
        this.haodian = haodian;
    }

    public int getGongshui() {
        return gongshui;
    }

    public void setGongshui(int gongshui) {
        this.gongshui = gongshui;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
